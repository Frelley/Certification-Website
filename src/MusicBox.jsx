const IDrum = ({samples : {key, url ,keyCode, id}, play, setText})=>{

 const handleKeydown = (e) => {
  if (e.keyCode === keyCode){
    const audio = document.getElementById(key);
    play(key);
   
    const newText =  firstSoundsGroup.filter((x)=>(e.keyCode === x.keyCode))[0].id
    setText(newText) 
    
    
    
  }
 }
  
  useEffect(()=>{
    document.addEventListener("keydown",handleKeydown)
  },[])
  
  return  <button 
  onClick={()=>play(key)} 
  className="drum-pad" 
 id={id}>{key}<audio 
  id={key} className="clip" 
  src={url}/></button>
}

const Drums = ({firstSoundsGroup , play , setText}) => {
  return firstSoundsGroup.map((samples) => <IDrum samples={samples}  key={samples.key} play={play} setText={setText}/>)
}

